let currentLang = localStorage.getItem('underus-lang') || document.documentElement.lang || 'fr';

function t(key) {
  return i18n[currentLang][key] ?? i18n.fr[key] ?? key;
}

function setLanguage(lang) {
  if (!i18n[lang]) return;
  currentLang = lang;
  localStorage.setItem('underus-lang', lang);
  document.documentElement.lang = lang;
  document.body.setAttribute('data-lang', lang);
  document.querySelectorAll('.legal-lang-block').forEach((block) => {
    block.hidden = block.getAttribute('data-lang') !== lang;
  });
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (key && t(key) !== key) el.textContent = t(key);
  });
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
  const titleEl = document.querySelector('title[data-i18n-title]');
  if (titleEl) titleEl.textContent = t(titleEl.getAttribute('data-i18n-title')) + ' – Under Us';
  if (typeof onLegalPageTranslate === 'function') onLegalPageTranslate();
}

document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => setLanguage(btn.getAttribute('data-lang')));
  });
});
